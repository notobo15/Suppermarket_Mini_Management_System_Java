    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:

        BUS_Customer tmp = new BUS_Customer();   
        if(jtf_ten.getText().equals(""))
            new AlertWarning("Vui lòng nhập tên khách hàng!!").setVisible(true);
        else if(jtf_sdt.getText().equals(""))
            new AlertWarning("Vui lòng nhập số điện thoại!!").setVisible(true);
        else
        {
            DTO_Customer cus = new DTO_Customer();
            cus.setName(jtf_ten.getText());
            cus.setPhone(jtf_sdt.getText());   
            if(jRadioButton1.isSelected())
            {
                cus.setGender("nam");
            }
            else
                cus.setGender("nu"); 
            String temp = new SimpleDateFormat("yyyy-MM-dd").format(jtf_ngaysinh.getDate());
            cus.setBirthDate(temp); 
            cus.setAddress(jtf_diachi.getText());  
                       
            try {
                String thanhcong = tmp.addCustomer(cus);
                if (thanhcong.equalsIgnoreCase("tao thanh cong")) {
                    this.dispose();
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(CustomerGUI.class.getName()).log(Level.SEVERE, null, ex);
            }            
        }
    }//GEN-LAST:event_jButton1MouseClicked

